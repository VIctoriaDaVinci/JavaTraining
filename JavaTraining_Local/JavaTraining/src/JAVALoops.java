
public class JAVALoops {

	public static void main(String[] args) {
		for (int i= 0; i < 15; i++) {
			
				System.out.println(i);
		}
		
		int f = 0;
				
		while (f <= 10){
			System.out.println(f);
		f++;
		}
		
		int j = 0;
		
		do{
			System.out.println(j);
		}while(j<0);
	}

}
